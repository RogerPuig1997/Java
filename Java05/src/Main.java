public class Main {
	public static void main (String[] args) {
		//Lógicos
		boolean Boolean = true;
		System.out.println(Boolean);
		//Caracter
		char Char = 35;
		System.out.println(Char);
		//Números enteros
		byte Byte = 10;
		short Short = 100;
		int Int = 1000;
		long Long = 1000000;
		System.out.println(Byte + " " + Short + " " + Int + " " + Long);
		//Números reales
		float Float = (float) 0.5;
		double Double = 125.34;
		System.out.println(Float + " " + Double);
		//Constante
		final int Final = 666;
		System.out.println(Final);
	}
}